     */
    public function store(Request $request)
    {
        $user = Auth::user();

        $message = Message::create([
            'user_id' => Auth::id(),
            'message' => $request->message,
        ]);
        event(new MessageSentEvent($user, $message));
        // return response()->json([
        //     'status' => 1,
        // ]);
        return ['status' => 'Message Sent!'];
    }

